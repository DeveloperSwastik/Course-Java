package module_01;

public class bDataTypes {
    public static void main(String[] args) {
        boolean var1 = false;
        byte var2 = 127;     //8 -128 to 127
        short var3 = 1;      //16 -32,768 to 32,767
        int var4 = 1;        //32 -2,147,483,648 to 2,147,483,647
        long var5 = 1;       //64 -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        float var6 = 1.4f;   // 6 to 7 decimal number
        double var7 = 1.1;   // 15 decimal number
        char var8 = '1';     // ASCII values
        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
        System.out.println(var4);
        System.out.println(var5);
        System.out.println(var6);
        System.out.println(var7);
        System.out.println(var8);
    }
}
